package model;

/**
 * L'enumerazione CardSeed rappresenta i semi delle carte in un mazzo di carte.
 * I semi comuni sono Fiori, Picche, Cuori e Quadri.
 */
public enum CardSeedModel {
    FIORI,   // Rappresenta il seme Fiori.
    PICCHE,  // Rappresenta il seme Picche.
    CUORI,   // Rappresenta il seme Cuori.
    QUADRI;   // Rappresenta il seme Quadri.
}
